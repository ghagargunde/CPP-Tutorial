//addition of two numbers

#include <iostream>
using namespace std;


int main(){
    int a,b;
    cout <<"Enter the value of a : "; // "<<" is called insertion operator   
    cin >>a;  // ">>" extraction operator
    cout <<"Enter the value of b : "; // "<<" is called insertion operator
    cin >>b;  // ">>" extraction operator

    cout <<"The sum of a and b is  "<<a+b <<".";
    return 0;
     
}